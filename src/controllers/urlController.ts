  // URL g√ºncelleme (ba≈ülƒ±k vs.)
  async updateUrl(req: AuthenticatedRequest, res: Response): Promise<void> {
    try {
      console.log('üîÑ UpdateUrl called with:', {
        params: req.params,
        body: req.body,
        userId: req.user?.userId
      });

      const { id } = req.params;
      const { title } = req.body;
      const userId = req.user?.userId;

      if (!title) {
        console.log('‚ùå Missing title in request body');
        res.status(400).json({
          success: false,
          error: 'Ba≈ülƒ±k gerekli'
        });
        return;
      }

      console.log('üîç Attempting to update URL:', { id, title, userId });
      const updatedUrl = await urlService.updateUrl(parseInt(id), { title }, userId);

      if (!updatedUrl) {
        console.log('‚ùå URL not found or no permission');
        res.status(404).json({
          success: false,
          error: 'URL bulunamadƒ± veya g√ºncelleme izniniz yok'
        });
        return;
      }

      console.log('‚úÖ URL updated successfully:', updatedUrl);
      res.json({
        success: true,
        data: updatedUrl
      });
    } catch (error) {
      console.error('‚ùå Error in updateUrl:', error);
      res.status(500).json({
        success: false,
        error: 'URL g√ºncellenirken hata olu≈ütu: ' + (error instanceof Error ? error.message : 'Bilinmeyen hata')
      });
    }
  }
