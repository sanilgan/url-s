  // URL'lerin listesini getir
  async listUrls(req: AuthenticatedRequest, res: Response): Promise<void> {
    try {
      console.log('üìã List URLs request received');
      console.log('üîê User ID:', req.user?.userId);
      console.log('üìù Request headers:', req.headers.authorization ? 'Has auth header' : 'No auth header');

      const limit = parseInt(req.query.limit as string) || 50;
      const offset = parseInt(req.query.offset as string) || 0;

      // Kullanƒ±cƒ± giri≈ü yapmƒ±≈ü olmalƒ± - yoksa bo≈ü array d√∂nd√ºr
      const userId = req.user?.userId;

      if (!userId) {
        console.log('üö´ No user logged in - returning empty array');
        res.json({
          success: true,
          data: []
        });
        return;
      }

      // Giri≈ü yapmƒ±≈ü kullanƒ±cƒ± - sadece kendi linklerini g√∂ster
      console.log('üë§ Fetching URLs for logged in user:', userId);
      const urls = await urlService.getUserUrls(userId, limit, offset);
      console.log('üìä User URLs found:', urls.length);

      const baseUrl = process.env.BASE_URL || 'http://localhost:3000';
      const urlsWithShortUrl = urls.map(url => ({
        ...url,
        short_url: `${baseUrl}/${url.short_code}`
      }));

      console.log('‚úÖ Sending response with', urlsWithShortUrl.length, 'URLs');
      console.log('üîó Sample URL:', urlsWithShortUrl[0] ? {
        id: urlsWithShortUrl[0].id,
        title: urlsWithShortUrl[0].title,
        short_url: urlsWithShortUrl[0].short_url,
        clicks: urlsWithShortUrl[0].clicks
      } : 'No URLs');

      res.json({
        success: true,
        data: urlsWithShortUrl
      });
    } catch (error) {
      console.error('‚ùå Error in listUrls:', error);
      res.status(500).json({
        success: false,
        error: 'URLler alƒ±nƒ±rken hata olu≈ütu'
      });
    }
  }

